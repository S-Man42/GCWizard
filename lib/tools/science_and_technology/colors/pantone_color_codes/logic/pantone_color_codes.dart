PantoneColor? _pantoneByRGB(GCWBaseColor rgb, List<PantoneColor> pantoneValues) {
  var hexCode = HexCode.fromRGB(rgb.toRGB());
  return pantoneValues.firstWhereOrNull((element) => element.colorcode.toLowerCase() == hexCode.toString().toLowerCase());
}

List<PantoneColor> findSimilarPantoneColors(GCWBaseColor rgb, List<PantoneColor> pantoneValues) {
  var pantone = _pantoneByRGB(rgb, pantoneValues);
  if (pantone != null) return [pantone];

  var pantoneRGBs = pantoneValues.map((element) => HexCode(element.colorcode).toRGB()).toList();

  List<PantoneColor> out = [];
  var distance = 0;
  while (distance < 100) {
    distance++;
    var nearestRGBs = findNearestRGBs(rgb, pantoneRGBs, distance: distance);

    if (nearestRGBs.length >= 5) {
      nearestRGBs.forEach((nearestRGB) {
        var nearestPantone = _pantoneByRGB(nearestRGB, pantoneValues);
        if (nearestPantone != null) return out.add(nearestPantone);
      });

      return out;
    }
  }

  return out;
}
